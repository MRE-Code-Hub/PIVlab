/*
 * build_search_index.mjs — regenerate assets/js/search-index.js from the pages.
 *
 * Optional convenience: the site works without ever running this. It scans
 * index.html and pages/*.html, reads each <h1>/<h2>/<h3>, maps the page to its
 * sidebar section via nav.js, and writes one search record per heading.
 *
 * Usage (from the manual/ folder):
 *     node tools/build_search_index.mjs
 *
 * Requires Node 16+. No external dependencies.
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

// --- Load the nav tree to map href -> section title --------------------------
const navSrc = readFileSync(join(root, "assets/js/nav.js"), "utf8");
const sectionOf = {};
{
  // crude but dependency-free: evaluate the object literal after the assignment
  const m = navSrc.match(/window\.MANUAL_NAV\s*=\s*(\{[\s\S]*\});/);
  if (m) {
    const nav = Function('"use strict";return (' + m[1] + ")")();
    for (const sec of nav.sections || []) {
      for (const it of sec.items || []) sectionOf[it.href] = sec.title;
    }
  }
}

// --- Collect page files ------------------------------------------------------
const pages = ["index.html", ...readdirSync(join(root, "pages"))
  .filter((f) => f.endsWith(".html")).map((f) => "pages/" + f)];

const slug = (s) =>
  s.toLowerCase().replace(/<[^>]+>/g, "").replace(/[^\w]+/g, "-").replace(/^-+|-+$/g, "");
const strip = (s) => s.replace(/<[^>]+>/g, "").replace(/\s+/g, " ").trim();

const records = [];
for (const href of pages) {
  const html = readFileSync(join(root, href), "utf8");
  const section = sectionOf[href] || "";
  const headings = [...html.matchAll(/<(h1|h2|h3)[^>]*\bid="([^"]+)"[^>]*>([\s\S]*?)<\/\1>/gi)];
  // also allow the first h1 without id
  const h1 = html.match(/<h1[^>]*>([\s\S]*?)<\/h1>/i);
  const pageTitle = h1 ? strip(h1[1]) : href;
  if (!headings.length) {
    records.push({ title: pageTitle, href, section, text: pageTitle.toLowerCase() });
    continue;
  }
  for (const [, , id, inner] of headings) {
    const title = strip(inner);
    records.push({
      title: title === pageTitle ? pageTitle : `${pageTitle} — ${title}`,
      href,
      hash: id,
      section,
      text: slug(title).replace(/-/g, " "),
    });
  }
}

const out =
  "/* AUTO-GENERATED by tools/build_search_index.mjs — you may edit by hand,\n" +
  "   but re-running the script will overwrite your changes. */\n" +
  "window.MANUAL_SEARCH = " + JSON.stringify(records, null, 2) + ";\n";

writeFileSync(join(root, "assets/js/search-index.js"), out);
console.log(`Wrote ${records.length} search records from ${pages.length} pages.`);
